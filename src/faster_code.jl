function (cs::CodegenState)(expr::Assignment)
    lhs = manual_dispatch_toexpr(expr.lhs, NameState(cs.rewrites))
    if Meta.isexpr(lhs, :call)
        scs, bm = enter_scope(cs)
        scs(expr.rhs)
        rhs = exit_scope!(scs, bm)
    else
        rhs = cs(expr.rhs)
    end
    return declare!(cs, lhs, rhs)
end

const ARGNAME_PREFIX = Vector{UInt8}("__argₛᵧₘ")

function add_arg_to_rewrites!(rewrites::Dict{Any, Any}, lhs::BasicSymbolic{T}) where {T}
    rewrites[lhs] = if get(rewrites, :readable_variables, false)::Bool
        Symbol(string(lhs)::String)
    else
        get_numbered_symbol_with_prefix(ARGNAME_PREFIX, hash(lhs))
    end
end

function (cs::CodegenState{T})(l::Let) where {T}
    if all(x->x isa Assignment && !(x.lhs isa DestructuredArgs), l.pairs)
        dargs = Vector{Assignment}(l.pairs)
    else
        dargs = Assignment[]
        for x in l.pairs
            handle_let_pair!(dargs, x, cs)
        end
    end

    if l.let_block
        scs, bm = enter_scope(cs)
        for asg in dargs
            lhs = asg.lhs
            if lhs isa BasicSymbolic{T} && iscall(lhs)
                add_arg_to_rewrites!(scs.rewrites, lhs)
            end
            scs(asg)
        end
        bindings = exit_scope!(scs, bm)

        scs, bm = enter_scope(cs)
        scs(l.body)
        body = exit_scope!(scs, bm)
        result = Expr(:let, bindings, body)
        return declare!(cs, get_misc_identifier(cs), result)
    end
    isempty(dargs) && return cs(l.body)

    for asg in dargs
        lhs = asg.lhs
        if lhs isa BasicSymbolic{T} && iscall(lhs)
            add_arg_to_rewrites!(cs.rewrites, lhs)
        end
        cs(asg)
    end

    return cs(l.body)
end

function (cs::CodegenState{T})(fn::Func) where {T}
    dargs = Union{Assignment, DestructuredArgs}[]
    for arg in fn.args
        if arg isa BasicSymbolic{T}
            add_arg_to_rewrites!(cs.rewrites, arg)
        elseif arg isa Assignment
            lhs = arg.lhs
            if lhs isa BasicSymbolic{T}
                add_arg_to_rewrites!(cs.rewrites, lhs)
            end
        elseif arg isa DestructuredArgs
            push!(dargs, arg)
        end
    end
    for kw in fn.kwargs
        lhs = kw.lhs
        if lhs isa BasicSymbolic{T}
            # Does not use `add_arg_to_rewrites!` because kwarg names matter
            cs.rewrites[lhs] = Symbol(string(lhs)::String)
        end
    end

    scs, bm = enter_scope(cs)
    scs(Let(dargs, fn.body, false))
    body = exit_scope!(scs, bm)

    fn_args = Expr(:tuple)
    if !isempty(fn.kwargs)
        fn_kws = Expr(:parameters)
        for asg in fn.kwargs
            lhs = manual_dispatch_toexpr(asg.lhs, NameState(cs.rewrites))
            asg_cs, bm = enter_scope(cs)
            asg_cs(asg.rhs)
            rhs = exit_scope!(asg_cs, bm)
            push!(fn_kws.args, Expr(:kw, lhs, rhs))
        end
        push!(fn_args.args, fn_kws)
    end

    for arg in fn.args
        if arg isa BasicSymbolic{T}
            push!(fn_args.args, cs.rewrites[arg]::Symbol)
        elseif arg isa Assignment
            asg_cs, bm = enter_scope(cs)
            asg_cs(arg.rhs)
            rhs = exit_scope!(asg_cs, bm)
            lhs = manual_dispatch_toexpr(arg.lhs, NameState(cs.rewrites))
            push!(fn_args.args, Expr(:(=), lhs, rhs))
        elseif arg isa DestructuredArgs
            name = arg.name
            if name isa BasicSymbolic{T}
                push!(fn_args.args, manual_dispatch_toexpr(name, NameState(cs.rewrites)))
            else
                push!(fn_args.args, name::Symbol)
            end
        else
            push!(fn_args.args, manual_dispatch_toexpr(arg, NameState(cs.rewrites)))
        end
    end

    fn_body = Expr(:block)
    append!(fn_body.args, fn.pre)
    push!(fn_body.args, body)
    return declare!(cs, get_misc_identifier(cs), Expr(:function, fn_args, fn_body))
end

function (cs::CodegenState)(ex::SetArray)
    arr = cs(ex.arr)::Symbol
    lhss = []
    rhss = Symbol[]
    for (i, elem) in enumerate(ex.elems)
        if elem isa AtIndex
            push!(lhss, cs(elem.i))
            push!(rhss, cs(elem.elem))
        else
            push!(lhss, i)
            push!(rhss, cs(elem))
        end
    end

    scs, bm = enter_scope(cs)
    for (lhs, rhs) in zip(lhss, rhss)
        declare!(scs, Expr(:ref, arr, lhs), rhs)
    end
    declare!(scs, get_misc_identifier(cs), ex.return_arr ? arr : nothing)
    body = exit_scope!(scs, bm)
    if ex.inbounds
        body = Expr(:macrocall, nameof(var"@inbounds"), LineNumberNode(0), body)
    end
    return declare!(cs, get_misc_identifier(cs), body)
end

function (cs::CodegenState)(ex::MakeArray)
    _similarto = ex.similarto
    if _similarto isa Type
        similarto = cs(_similarto)
    else
        similarto = declare!(cs, get_misc_identifier(cs), Expr(:call, typeof, cs(_similarto)))
    end
    ndim = ndims(ex.elems)::Int
    elT = ex.output_eltype
    result = Expr(
        :call, create_array, similarto, elT, Expr(:call, Expr(:curly, Val, ndim)),
        Expr(:call, Expr(:curly, Val, size(ex.elems)))
    )
    for x in ex.elems
        push!(result.args, cs(x))
    end
    return declare!(cs, get_misc_identifier(cs), result)
end

function (cs::CodegenState{T})(a::MakeSparseArray{<:SparseMatrixCSC}) where {T}
    I, J, V = findnz(a.array)
    m, n = size(a.array)
    result = Expr(:call, sparse, I, J, cs(Const{T}(V)), m, n)
    return declare!(cs, get_misc_identifier(cs), result)
end

function (cs::CodegenState{T})(a::MakeSparseArray{<:SparseVector}) where {T}
    I, V = findnz(a.array)
    n = length(a.array)
    result = Expr(:call, SparseVector, n, I, cs(Const{T}(V)))
    return declare!(cs, get_misc_identifier(cs), result)
end

function (cs::CodegenState)(a::MakeTuple)
    result = Expr(:tuple)
    for ex in a.elems
        push!(result.args, cs(ex))
    end
    return declare!(cs, get_misc_identifier(cs), result)
end

function (cs::CodegenState)(ex::LiteralExpr)
    declare!(cs, get_misc_identifier(cs), recurse_expr(ex.ex, NameState(cs.rewrites)))
end

function (cs::CodegenState)(f::ForLoop)
    st = NameState(cs.rewrites)
    result = Expr(
        :for, Expr(
            :(=), manual_dispatch_toexpr(f.itervar, st), manual_dispatch_toexpr(f.range, st)
        )
    )
    scs, bm = enter_scope(cs)
    scs(f.body)
    body = exit_scope!(scs, bm)
    push!(result.args, body)
    return declare!(cs, get_misc_identifier(cs), result)
end

function codegen_function!(::Type{Let}, cs::CodegenState{T}, expr::BasicSymbolic{T}, expr_idx::Integer) where {T}
    nbors = Graphs.outneighbors(cs.ir.dependency_graph, expr_idx)
    n = length(nbors)
    body_idx = nbors[n]

    # Codegen each symAssignment as a side effect in order
    for i in 1:n-1
        cs(cs.ir[nbors[i]])
    end

    return codegen!(cs, expr_idx, cs(cs.ir[body_idx]))
end

function codegen_function!(::Type{Assignment}, cs::CodegenState{T}, expr::BasicSymbolic{T}, expr_idx::Integer) where {T}
    nbors = Graphs.outneighbors(cs.ir.dependency_graph, expr_idx)
    lhs_expr = cs.ir[nbors[1]]
    rhs = cs.ir[nbors[2]]

    lhs = manual_dispatch_toexpr(lhs_expr, NameState(cs.rewrites))
    if Meta.isexpr(lhs, :call)
        scs, bm = enter_scope(cs)
        scs(rhs)
        rhs_result = exit_scope!(scs, bm)
    else
        rhs_result = cs(rhs)
    end
    declare!(cs, lhs, rhs_result)
    return codegen!(cs, expr_idx, lhs)
end

function codegen_function!(::Type{Func}, cs::CodegenState{T}, expr::BasicSymbolic{T}, expr_idx::Integer) where {T}
    nbors = Graphs.outneighbors(cs.ir.dependency_graph, expr_idx)
    args_term_idx = nbors[1]
    body_idx = nbors[2]

    args_nbors = Graphs.outneighbors(cs.ir.dependency_graph, args_term_idx)
    n_args = length(args_nbors)
    body = cs.ir[body_idx]

    # Walk the args once to: register rewrites and collect DestructuredArgs info.
    # Each entry is (sym_registered_in_rewrites, old_value) for later restoration.
    rewrites_to_restore = Pair{BasicSymbolic{T}, Any}[]
    # (name, elems): array param + the element variables to bind inside the body
    dargs_info = Tuple{BasicSymbolic{T}, Vector{BasicSymbolic{T}}}[]

    for i in 1:n_args
        arg = cs.ir[args_nbors[i]]
        @match arg begin
            BSImpl.Sym(;) => begin
                push!(rewrites_to_restore, arg => get(cs.rewrites, arg, nothing))
                add_arg_to_rewrites!(cs.rewrites, arg)
            end
            BSImpl.Term(; f) && if f === DestructuredArgs end => begin
                da_nbors = Graphs.outneighbors(cs.ir.dependency_graph, args_nbors[i])
                name = cs.ir[da_nbors[1]]
                elems = BasicSymbolic{T}[cs.ir[da_nbors[j]] for j in 2:length(da_nbors)]
                push!(rewrites_to_restore, name => get(cs.rewrites, name, nothing))
                add_arg_to_rewrites!(cs.rewrites, name)
                push!(dargs_info, (name, elems))
            end
            _ => nothing
        end
    end

    scs, bm = enter_scope(cs)

    # Emit destructuring bindings — elem_i = array_param[i] — before the body
    for (name, elems) in dargs_info
        name_sym = cs.rewrites[name]::Symbol
        for (i, elem) in enumerate(elems)
            elem_name = manual_dispatch_toexpr(elem, NameState(scs.rewrites))
            declare!(scs, elem_name, Expr(:ref, name_sym, i))
        end
    end

    scs(body)
    fn_body_block = exit_scope!(scs, bm)

    fn_args = Expr(:tuple)
    for i in 1:n_args
        arg = cs.ir[args_nbors[i]]
        @match arg begin
            BSImpl.Sym(;) => push!(fn_args.args, cs.rewrites[arg]::Symbol)
            BSImpl.Term(; f) && if f === DestructuredArgs end => begin
                da_nbors = Graphs.outneighbors(cs.ir.dependency_graph, args_nbors[i])
                name = cs.ir[da_nbors[1]]
                push!(fn_args.args, cs.rewrites[name]::Symbol)
            end
            _ => push!(fn_args.args, manual_dispatch_toexpr(arg, NameState(cs.rewrites)))
        end
    end

    result = codegen!(cs, expr_idx, Expr(:function, fn_args, Expr(:block, fn_body_block)))

    # Restore rewrites to their state before this call
    for (sym, old_val) in rewrites_to_restore
        if old_val === nothing
            delete!(cs.rewrites, sym)
        else
            cs.rewrites[sym] = old_val
        end
    end

    return result
end
